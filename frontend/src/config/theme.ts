// Type exports for TypeScript
export type Theme = typeof theme;
export type Colors = typeof theme.colors;
export type ColorShade = keyof typeof theme.colors.primary;

// Helper function to get color with shade
export const getColor = (color: keyof Colors, shade: ColorShade = 500) => {
  const colorObj = theme.colors[color];
  if (typeof colorObj === 'object' && shade in colorObj) {
    return colorObj[shade as keyof typeof colorObj];
  }
  return colorObj;
};

